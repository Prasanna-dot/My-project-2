const mouseWheel = document.querySelector('.box-scroll');

mouseWheel.addEventListener('wheel', function (e) {
  const race = 15;

  if (e.deltaY > 0)
    mouseWheel.scrollLeft += race;
  else
    mouseWheel.scrollLeft -= race;
  e.preventDefault();
});



