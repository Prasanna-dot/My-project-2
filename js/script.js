let idleTimer = null;
let idleState = false;

function showFoo(time) {
  clearTimeout(idleTimer);
  if (idleState == true) {
    $(".foo").removeClass("inactive");
  }
  idleState = false;
  idleTimer = setTimeout(function () {
    $(".foo").addClass("inactive");
    idleState = true;
  }, time);
}

showFoo(1000);

$(window).mousemove(function () {
  showFoo(1000);
});

